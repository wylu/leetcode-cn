/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func flatten(root *TreeNode) {
	if root == nil {
		return
	}

	for root != nil {
		if root.Left == nil {
			root = root.Right
			continue
		}

		// 找到左子树的最右结点
		pre := root.Left
		for pre.Right != nil {
			pre = pre.Right
		}

		pre.Right = root.Right
		root.Right = root.Left
		root.Left = nil
		root = root.Right
	}
}

