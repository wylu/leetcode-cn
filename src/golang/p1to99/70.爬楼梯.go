// @lc code=start
func climbStairs(n int) int {
	f := [2]int{1, 1}
	if n < 2 {
		return f[n]
	}

	for i := 2; i <= n; i++ {
		f[0], f[1] = f[1], f[0]+f[1]
	}
	return f[1]
}

