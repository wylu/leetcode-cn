#include <bits/stdc++.h>
using namespace std;

// @lc code=start
class Solution {
public:
    int maxSubArray(vector<int>& nums) {
        int ans = INT32_MIN, cur = 0;
        for (auto num : nums) {
            if (cur <= 0) {
                cur = num;
            } else {
                cur += num;
            }
            ans = max(ans, cur);
        }
        return ans;
    }
};
