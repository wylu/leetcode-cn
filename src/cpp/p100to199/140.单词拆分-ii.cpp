#include <bits/stdc++.h>
using namespace std;

// @lc code=start
class Solution {
    unordered_map<int, vector<string>> ans;
    unordered_set<string> wordSet;

public:
    vector<string> wordBreak(string s, vector<string>& wordDict) {
        wordSet = unordered_set(wordDict.begin(), wordDict.end());
        backtrack(s, 0);
        return ans[0];
    }

    void backtrack(const string& s, int cur) {
        if (ans.count(cur)) return;
        if (cur == s.length()) {
            ans[cur] = {""};
            return;
        }

        ans[cur] = {};
        for (int i = cur + 1; i <= s.length(); i++) {
            string word = s.substr(cur, i - cur);
            if (wordSet.count(word)) {
                backtrack(s, i);
                for (const string& stc : ans[i]) {
                    ans[cur].push_back(stc.empty() ? word : word + " " + stc);
                }
            }
        }
    }
};
