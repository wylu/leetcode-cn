#include <bits/stdc++.h>
using namespace std;

// @lc code=start
class Solution {
public:
    int maximumProduct(vector<int>& nums) {
        sort(nums.begin(), nums.end());
        int n = nums.size();
        return max(nums[0] * nums[1] * nums[n - 1],
                   max(nums[0] * nums[1] * nums[2],
                       nums[n - 3] * nums[n - 2] * nums[n - 1]));
    }
};
